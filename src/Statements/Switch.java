package Statements;

public class Switch  {

	  public static void main(String[] args) {
	    int i = 2;
	    switch (i) {
	    case 1 :
	        System.out.println("a+b");
	        break;
	    case 2 :
	        System.out.println("a*b");
	        break;
	    case 3 :
	        System.out.println("a/b");
	        break;
	    default:
	        System.out.println("You did not enter a valid value.");
	    }
	  }

	}
